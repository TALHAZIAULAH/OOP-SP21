
package l3activity2;


public class Rectangle3 {
    public int length, width;
    public Rectangle3()
    {
    length = 5; width = 2;
    }
    public Rectangle3(int l, int w)
    {
    length = l; width = w;
    }
    public int Calculatearea ()
    {
    return (length*width);
    } 
    
    
}
